import {
  KEM_DHKEM_P256_HKDF_SHA256,
  KEM_DHKEM_P384_HKDF_SHA384,
  KEM_DHKEM_P521_HKDF_SHA512,
  KEM_DHKEM_X25519_HKDF_SHA256,
  KEM_DHKEM_X448_HKDF_SHA512,
  KDF_HKDF_SHA256,
  KDF_HKDF_SHA384,
  KDF_HKDF_SHA512,
  AEAD_AES_128_GCM,
  AEAD_AES_256_GCM,
  AEAD_ChaCha20Poly1305,
} from "hpke";

const suites = [
  [KEM_DHKEM_P256_HKDF_SHA256, KDF_HKDF_SHA256, AEAD_AES_128_GCM],
  [KEM_DHKEM_P384_HKDF_SHA384, KDF_HKDF_SHA384, AEAD_AES_256_GCM],
  [KEM_DHKEM_P521_HKDF_SHA512, KDF_HKDF_SHA512, AEAD_AES_256_GCM],
  [KEM_DHKEM_X25519_HKDF_SHA256, KDF_HKDF_SHA256, AEAD_AES_128_GCM],
  [KEM_DHKEM_X25519_HKDF_SHA256, KDF_HKDF_SHA256, AEAD_ChaCha20Poly1305],
  [KEM_DHKEM_X448_HKDF_SHA512, KDF_HKDF_SHA512, AEAD_AES_256_GCM],
  [KEM_DHKEM_X448_HKDF_SHA512, KDF_HKDF_SHA512, AEAD_ChaCha20Poly1305],
  [KEM_DHKEM_P256_HKDF_SHA256, KDF_HKDF_SHA256, AEAD_AES_256_GCM],
];

export const algorithms = suites.flatMap(([kem, kdf, aead], index) => {
  const alg = `HPKE-${index}`;
  const base = { alg, kem, kdf, aead };
  const ke = { alg: `${alg}-KE`, kem, kdf, aead };
  return [base, ke];
});
